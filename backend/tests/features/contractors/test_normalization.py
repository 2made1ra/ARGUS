import pytest

from app.features.contractors.normalization import normalize_name


@pytest.mark.parametrize(
    "raw,expected",
    [
        ("ООО Вектор", "вектор"),
        ('ООО "Вектор"', "вектор"),
        ("АО Ромашка", "ромашка"),
        ("ИП Иванов", "иванов"),
        ("ПАО Газпром", "газпром"),
        ("ЗАО Нефть", "нефть"),
        ("НКО Добро", "добро"),
        ("ооо вектор", "вектор"),
        ("  лишние   пробелы  ", "лишние пробелы"),
        ("Вектор, Системы!", "вектор системы"),
        ("Иванов Иван", "иван иванов"),
        ("Иванов Иван Иванович", "иван иванов иванович"),
        ("Вектор Технологии Системы Интеграция", "вектор технологии системы интеграция"),
        ("", ""),
    ],
)
def test_normalize_name(raw: str, expected: str) -> None:
    assert normalize_name(raw) == expected
